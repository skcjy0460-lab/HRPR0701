def render_ai_consulting_page():
    st.markdown("""
    <div class="page-header">
      <div class="header-icon">🤖</div>
      <div>
        <h1>AI 인사노무 상담</h1>
        <p>병원 인사노무에 특화된 AI가 근로기준법·4대보험·의료법 관련 질문에 답변합니다.</p>
      </div>
    </div>
    """, unsafe_allow_html=True)

    st.markdown("""
    <div class="warn-box">
      ⚠️ <b>법적 효력 없음:</b> AI 상담 결과는 참고용이며 법적 효력을 갖지 않습니다.
      중요한 의사결정이나 법적 분쟁 소지가 있는 사안은 반드시 <b>공인노무사</b> 또는 <b>관할 고용노동청</b>에 문의하세요.
    </div>
    """, unsafe_allow_html=True)

    # ── 빠른 질문 선택
    with st.expander("💡 자주 묻는 질문 빠른 선택 (클릭하면 자동 입력됩니다)", expanded=False):
        cols = st.columns(2)
        for i, q in enumerate(QUICK_QUESTIONS):
            with cols[i % 2]:
                if st.button(q, key=f"quick_q_{i}", use_container_width=True):
                    st.session_state["ai_quick_input"] = q

    # ── 채팅 히스토리 초기화
    if "ai_chat_history" not in st.session_state:
        st.session_state.ai_chat_history = [
            {
                "role": "assistant",
                "content": "안녕하세요! 👋 저는 병원 인사노무 전문 AI 어시스턴트입니다.\n\n"
                           "**근로계약, 연차, 퇴직금, 4대보험, 수당 계산, 법정의무교육** 등 병원 인사노무 실무와 관련된 질문을 자유롭게 해주세요.\n\n"
                           "위의 '자주 묻는 질문'을 클릭하거나, 직접 질문을 입력하세요.",
            }
        ]

    # ── 채팅 히스토리 표시
    for msg in st.session_state.ai_chat_history:
        with st.chat_message(msg["role"]):
            st.markdown(msg["content"])

    # ── 입력창 (빠른 질문 선택 시 자동 입력)
    default_input = st.session_state.pop("ai_quick_input", "")
    user_input = st.chat_input("질문을 입력하세요...", )

    # 빠른 질문이 선택된 경우 처리
    if default_input and not user_input:
        user_input = default_input

    if user_input:
        # 사용자 메시지 추가
        st.session_state.ai_chat_history.append({"role": "user", "content": user_input})
        with st.chat_message("user"):
            st.markdown(user_input)

        # AI 응답 생성
        with st.chat_message("assistant"):
            message_placeholder = st.empty()

            if OPENAI_AVAILABLE:
                try:
                    client = OpenAI()
                    messages = [{"role": "system", "content": SYSTEM_PROMPT}]
                    # 최근 10개 대화만 포함 (컨텍스트 관리)
                    history = st.session_state.ai_chat_history[-10:]
                    for msg in history:
                        messages.append({"role": msg["role"], "content": msg["content"]})

                    with st.spinner("AI가 답변을 생성 중입니다..."):
                        response = client.chat.completions.create(
                            model="claude-haiku-4-5",
                            messages=messages,
                            stream=False,
                            max_tokens=2000,
                            temperature=0.3,
                        )
                    full_response = response.choices[0].message.content
                    if not full_response:
                        full_response = _fallback_response(user_input)
                    message_placeholder.markdown(full_response)

                except Exception as e:
                    full_response = _fallback_response(user_input)
                    message_placeholder.markdown(full_response)
            else:
                full_response = _fallback_response(user_input)
                message_placeholder.markdown(full_response)

        st.session_state.ai_chat_history.append({"role": "assistant", "content": full_response})

    # ── 대화 초기화 버튼
    if len(st.session_state.ai_chat_history) > 1:
        if st.button("🗑️ 대화 초기화", use_container_width=False):
            st.session_state.ai_chat_history = [st.session_state.ai_chat_history[0]]
            st.rerun()


